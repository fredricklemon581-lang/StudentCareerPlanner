def get_knowledge_points_for_subject(subject_name):
    """获取指定科目的所有知识点"""
    return KNOWLEDGE_POINTS.get(subject_name, [])

def get_total_knowledge_points():
    """获取所有知识点总数"""
    total = 0
    for subject_kps in KNOWLEDGE_POINTS.values():
        total += len(subject_kps)
    return total

